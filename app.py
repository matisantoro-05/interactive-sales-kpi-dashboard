# 10. INTERACTIVE TABLE + CSV DOWNLOAD
# ══════════════════════════════════════════════════════════════════════════════
st.subheader("📋 Filtered Order Details")

# Format monetary columns for display
df_display = df[[
    "Fecha", "Producto", "Región", "Unidades", "Ventas_USD", "Costo_USD", "Ganancia_USD"
]].copy()
df_display["Fecha"] = df_display["Fecha"].dt.strftime("%d/%m/%Y")

# Show table with fixed height and scroll
st.dataframe(
    df_display.sort_values("Ventas_USD", ascending=False).reset_index(drop=True),
    use_container_width=True,
    height=320,
    column_config={
        "Ventas_USD":   st.column_config.NumberColumn("Revenue (USD)", format="$%.2f"),
        "Costo_USD":    st.column_config.NumberColumn("Cost (USD)",    format="$%.2f"),
        "Ganancia_USD": st.column_config.NumberColumn("Profit (USD)",  format="$%.2f"),
        "Unidades":     st.column_config.NumberColumn("Units",         format="%d"),
    },
)

# ── Download button ───────────────────────────────────────────────────────────
csv_bytes = df_display.to_csv(index=False, sep=";", decimal=",").encode("utf-8-sig")

col_dl, col_info = st.columns([1, 4])
with col_dl:
    st.download_button(
        label="⬇️  Download filtered CSV",
        data=csv_bytes,
        file_name=f"sales_{date_start}_{date_end}.csv",
        mime="text/csv",
        use_container_width=True,
    )
with col_info:
    st.caption(
        f"📄  {len(df_display):,} rows  ·  "
        f"Period: {date_start.strftime('%d/%m/%Y')} → {date_end.strftime('%d/%m/%Y')}"
    )


