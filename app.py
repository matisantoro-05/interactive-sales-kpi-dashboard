# ══════════════════════════════════════════════════════════════════════════════
# 1. DATA LOADING & CACHE
# ══════════════════════════════════════════════════════════════════════════════
DATA_FILE = Path("ventas_demo.csv")


@st.cache_data(show_spinner="Loading data…")
def load_data(path: Path) -> pd.DataFrame:
    """Read CSV, validate columns and apply correct types."""
    required_cols = {"Fecha", "Producto", "Región", "Ventas_USD", "Unidades", "Costo_USD"}
    df = pd.read_csv(path, parse_dates=["Fecha"])
    missing = required_cols - set(df.columns)
    if missing:
        raise ValueError(f"Missing columns in CSV: {missing}")
    df["Ganancia_USD"] = df["Ventas_USD"] - df["Costo_USD"]
    df["Mes"]          = df["Fecha"].dt.to_period("M").dt.to_timestamp()
    return df


# ── Load data ─────────────────────────────────────────────────────────────────
if not DATA_FILE.exists():
    st.error(
        "⚠️  **ventas_demo.csv** not found. "
        "Please run `python generate_data.py` first to generate the demo data."
    )
    st.stop()

try:
    df_raw = load_data(DATA_FILE)
except Exception as exc:
    st.error(f"Error reading data file: {exc}")
    st.stop()


# ══════════════════════════════════════════════════════════════════════════════
# 2. SIDEBAR  ─  Interactive filters
# ══════════════════════════════════════════════════════════════════════════════
with st.sidebar:
    st.image(
        "https://img.icons8.com/color/96/combo-chart.png",
        width=64,
    )
    st.title("Sales Dashboard")
    st.caption("MVP · v1.0")
    st.divider()

    # ── Filter: Date range ────────────────────────────────────────────────────
    st.subheader("📅 Date Range")
    min_date = df_raw["Fecha"].min().date()
    max_date = df_raw["Fecha"].max().date()

    date_start, date_end = st.date_input(
        "Select period",
        value=(min_date, max_date),
        min_value=min_date,
        max_value=max_date,
        format="DD/MM/YYYY",
    )

    st.divider()

    # ── Filter: Region ────────────────────────────────────────────────────────
    st.subheader("🌎 Region")
    all_regions  = sorted(df_raw["Región"].unique())
    sel_regions  = st.multiselect(
        "Select regions",
        options=all_regions,
        default=all_regions,
        placeholder="Choose one or more…",
    )

    st.divider()

    # ── Filter: Product ───────────────────────────────────────────────────────
    st.subheader("📦 Product")
    all_products = sorted(df_raw["Producto"].unique())
    sel_products = st.multiselect(
        "Select products",
        options=all_products,
        default=all_products,
        placeholder="Choose one or more…",
    )

    st.divider()
    st.caption("Demo data · 2024")


# ══════════════════════════════════════════════════════════════════════════════
# 3. APPLY FILTERS
# ══════════════════════════════════════════════════════════════════════════════
# Handle the case where date_input returns a single value (incomplete range)
if isinstance(date_start, date) and isinstance(date_end, date):
    pass
else:
    date_start = min_date
    date_end   = max_date

df = df_raw[
    (df_raw["Fecha"].dt.date >= date_start) &
    (df_raw["Fecha"].dt.date <= date_end)   &
    (df_raw["Región"].isin(sel_regions  if sel_regions  else all_regions))  &
    (df_raw["Producto"].isin(sel_products if sel_products else all_products))
].copy()


# ══════════════════════════════════════════════════════════════════════════════
# 4. VALIDATION: Empty DataFrame
# ══════════════════════════════════════════════════════════════════════════════
if df.empty:
    st.warning(
        "⚠️  No results found for the selected filters. "
        "Try adjusting the date range or the region / product filters."
    )
    st.stop()


# ══════════════════════════════════════════════════════════════════════════════
# 5. DASHBOARD HEADER
# ══════════════════════════════════════════════════════════════════════════════
st.markdown(
    f"""
    <div class="dash-header">
        <h1 style="color:{TEXT}; margin:0; font-size:28px;">
            📊 Sales Dashboard · 2024
        </h1>
        <p style="color:#94A3B8; margin:4px 0 0 0; font-size:14px;">
            Period: {date_start.strftime('%d %b %Y')} → {date_end.strftime('%d %b %Y')}
            &nbsp;·&nbsp; {len(df):,} orders shown
        </p>
    </div>
    """,
    unsafe_allow_html=True,
)


# ══════════════════════════════════════════════════════════════════════════════
# 6. MAIN KPIs
# ══════════════════════════════════════════════════════════════════════════════
total_ventas   = df["Ventas_USD"].sum()
total_ganancia = df["Ganancia_USD"].sum()
total_pedidos  = len(df)
margen_pct     = (total_ganancia / total_ventas * 100) if total_ventas else 0
total_unidades = df["Unidades"].sum()

# Share vs. full period to compute delta
pct_del_total  = (total_ventas / df_raw["Ventas_USD"].sum() * 100)

kpi1, kpi2, kpi3, kpi4 = st.columns(4)

def render_kpi(col, label: str, value: str, delta: str, up: bool):
    direction = "up" if up else "down"
    arrow     = "▲" if up else "▼"
    col.markdown(
        f"""
        <div class="kpi-card">
            <div class="kpi-label">{label}</div>
            <div class="kpi-value">{value}</div>
            <div class="kpi-delta {direction}">{arrow} {delta}</div>
        </div>
        """,
        unsafe_allow_html=True,
    )

with kpi1:
    render_kpi(
        kpi1,
        "💰 Total Revenue",
        f"${total_ventas:,.0f}",
        f"{pct_del_total:.1f}% of full year",
        up=True,
    )
with kpi2:
    render_kpi(
        kpi2,
        "📈 Net Profit",
        f"${total_ganancia:,.0f}",
        f"Margin {margen_pct:.1f}%",
        up=margen_pct >= 30,
    )
with kpi3:
    render_kpi(
        kpi3,
        "🛒 Total Orders",
        f"{total_pedidos:,}",
        f"Avg. ticket ${total_ventas/total_pedidos:,.0f}",
        up=True,
    )
with kpi4:
    render_kpi(
        kpi4,
        "📦 Units Sold",
        f"{total_unidades:,}",
        f"Avg. {total_unidades/total_pedidos:.1f} u/order",
        up=True,
    )

st.markdown("<br>", unsafe_allow_html=True)


# ══════════════════════════════════════════════════════════════════════════════
# 7. CHARTS  ─  Row 1: Bar by Product  +  Donut by Region
# ══════════════════════════════════════════════════════════════════════════════
col_bar, col_pie = st.columns([3, 2], gap="large")

# ── 7a. Bar chart: Revenue by Product ────────────────────────────────────────
with col_bar:
    st.subheader("🏆 Revenue by Product")

    df_prod = (
        df.groupby("Producto", as_index=False)
          .agg(Ventas=("Ventas_USD", "sum"), Ganancia=("Ganancia_USD", "sum"))
          .sort_values("Ventas", ascending=True)
    )

    fig_bar = go.Figure()
    fig_bar.add_trace(go.Bar(
        y=df_prod["Producto"],
        x=df_prod["Ventas"],
        name="Revenue",
        orientation="h",
        marker_color=PRIMARY,
        text=df_prod["Ventas"].apply(lambda v: f"${v:,.0f}"),
        textposition="outside",
    ))
    fig_bar.add_trace(go.Bar(
        y=df_prod["Producto"],
        x=df_prod["Ganancia"],
        name="Profit",
        orientation="h",
        marker_color=SECONDARY,
        text=df_prod["Ganancia"].apply(lambda v: f"${v:,.0f}"),
        textposition="outside",
        visible="legendonly",
    ))
    fig_bar.update_layout(
        template=PLOTLY_TEMPLATE,
        barmode="overlay",
        height=340,
        margin=dict(l=0, r=60, t=10, b=10),
        legend=dict(orientation="h", yanchor="bottom", y=1.02, xanchor="right", x=1),
        xaxis_title="USD",
        paper_bgcolor="rgba(0,0,0,0)",
        plot_bgcolor="rgba(0,0,0,0)",
        font=dict(color=TEXT),
    )
    st.plotly_chart(fig_bar, use_container_width=True)

# ── 7b. Donut: Distribution by Region ────────────────────────────────────────
with col_pie:
    st.subheader("🌎 Share by Region")

    df_reg = (
        df.groupby("Región", as_index=False)["Ventas_USD"]
          .sum()
          .sort_values("Ventas_USD", ascending=False)
    )

    fig_pie = px.pie(
        df_reg,
        names="Región",
        values="Ventas_USD",
        hole=0.52,
        color_discrete_sequence=COLOR_SEQ,
        template=PLOTLY_TEMPLATE,
    )
    fig_pie.update_traces(
        textposition="outside",
        textinfo="label+percent",
    )
    fig_pie.update_layout(
        height=340,
        margin=dict(l=10, r=10, t=10, b=10),
        showlegend=False,
        paper_bgcolor="rgba(0,0,0,0)",
        font=dict(color=TEXT),
    )
    st.plotly_chart(fig_pie, use_container_width=True)


# ══════════════════════════════════════════════════════════════════════════════
# 8. CHART  ─  Row 2: Monthly sales trend
# ══════════════════════════════════════════════════════════════════════════════
st.subheader("📆 Monthly Sales Trend")

df_monthly = (
    df.groupby(["Mes", "Producto"], as_index=False)["Ventas_USD"]
      .sum()
)

fig_line = px.line(
    df_monthly,
    x="Mes",
    y="Ventas_USD",
    color="Producto",
    markers=True,
    labels={"Ventas_USD": "Revenue (USD)", "Mes": "Month", "Producto": "Product"},
    template=PLOTLY_TEMPLATE,
    color_discrete_sequence=COLOR_SEQ,
)
fig_line.update_traces(line_width=2.5, marker_size=7)
fig_line.update_layout(
    height=380,
    margin=dict(l=0, r=0, t=10, b=10),
    legend=dict(orientation="h", yanchor="bottom", y=1.02, xanchor="right", x=1),
    xaxis_title="",
    yaxis_title="Revenue (USD)",
    paper_bgcolor="rgba(0,0,0,0)",
    plot_bgcolor="rgba(0,0,0,0)",
    font=dict(color=TEXT),
    xaxis=dict(gridcolor="#1E293B"),
    yaxis=dict(gridcolor="#334155"),
)
st.plotly_chart(fig_line, use_container_width=True)


# ══════════════════════════════════════════════════════════════════════════════
# 9. HEATMAP  ─  Revenue by Region × Month
# ══════════════════════════════════════════════════════════════════════════════
st.subheader("🗺️ Heat Map: Revenue by Region and Month")

df_heat = (
    df.groupby(["Mes", "Región"])["Ventas_USD"]
      .sum()
      .reset_index()
      .pivot(index="Región", columns="Mes", values="Ventas_USD")
      .fillna(0)
)
df_heat.columns = [c.strftime("%b %Y") for c in df_heat.columns]

fig_heat = px.imshow(
    df_heat,
    color_continuous_scale="Blues",
    aspect="auto",
    template=PLOTLY_TEMPLATE,
    labels=dict(color="Revenue (USD)"),
)
fig_heat.update_layout(
    height=280,
    margin=dict(l=0, r=0, t=10, b=10),
    paper_bgcolor="rgba(0,0,0,0)",
    font=dict(color=TEXT),
    coloraxis_colorbar=dict(title="USD"),
)
st.plotly_chart(fig_heat, use_container_width=True)


